package org.areska.starhopper;

public class Star
{
    double declinationRadians;
    double ascensionRadians;
    double magnitude;
    
    public double getDeclinationRadians()
    {
        return declinationRadians;
    }
    public void setDeclinationRadians(double declinationRadians)
    {
        this.declinationRadians = declinationRadians;
    }
    public double getAscensionRadians()
    {
        return ascensionRadians;
    }
    public void setAscensionRadians(double ascensionRadians)
    {
        this.ascensionRadians = ascensionRadians;
    }
    public double getMagnitude()
    {
        return magnitude;
    }
    public void setMagnitude(double magnitude)
    {
        this.magnitude = magnitude;
    }

}
